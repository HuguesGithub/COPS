<article class="card col-12">
  <div class="card-body">
    Cette section décrit les différentes bases de données accessibles aux cops, avec plus ou moins de facilité. Hormis pour le Network lui-même, aucune de ces bases de données n'est directement reliée à ce dernier ; elles sont cependant accessibles depuis n'importe quel ordinateur relié au réseau interne du LAPD. Toutefois, pour éviter des allers-retours incessants pour les Détectives en mission, une version allégée de certaines de ces bases est enregistrée chaque matin dans les véhicules du LAPD, et est accessible via le MPTS.<br><br>
    <p>
      <label>MPTS</label> : ordinateur de bord des voitures du LAPD. Installé dans le tableau de bord, l'écran tactile au milieu. Un clavier est disponible sous la boîte à gants. Les infos disponibles sont les suivantes :
      <ul>
        <li>Plan de LA avec positionnement du véhicule en temps réel et aide à la circulation.</li>
        <li>Contrôle de la radio.</li>
        <li>Boite à messages audio et vidéo.</li>
        <li>Historique des messages envoyés par les anges à la voiture.</li>
        <li>Visionner les caméras avant et arrière de la voiture.</li>
        <li>Aide juridique.</li>
        <li>Agenda et traitement de texte.</li>
        <li>Accès aux bases de données allégées : FCID, DABIS, MSD, CCF, CHAD.</li>
      </ul>
    </p>
    Toutes les bases de données sont décrites selon le même format :
    <ul>
      <li><label>Administrateur</label> : l'organisme, public ou non, qui gère la base de données. C'est lui qui en surveille l'accès et qui décide qui peut y accéder ou y mettre à jour les informations.</li>
      <li><label>Accès</label> : qui peut accéder à cette base de données et comment.</li>
      <li><label>Temps de recherche</label> : la durée d'une recherche d'information. Chaque point de marge de réussite sur le jet de recherche permettra de réduire de 5mn le temps de recherche.</li>
      <li><label>Informations disponibles</label> : les informations consignées dans la base de données. Chaque type d'information est suivi d'une série de deux chiffres séparées d'un "/". Le premier indique le niveau d'information - comme pour les contacts - et le second la difficulté du jet d'Informatique/Éducation pour trouver une information de ce niveau.</li>
    </ul>
    Un petit texte descriptif permet ensuite de se faire une meilleure idée de l'utilisation ou de la gestion de cette base de données.<br>
    N'oubliez pas non plus qu'un certain nombre d'informations disponibles sur les banques de données gouvernementales requièrent tout de même un mandat (c'est notamment le cas de la plupart des informations de niveau 3 et plus).
  </div>
